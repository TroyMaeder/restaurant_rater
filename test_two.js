function retrieveWords(restaurantList, userInput) {
  var suggestedRestaurants = [];
  for (var i = 0; i < restaurantList.length; i++) {
    if (restaurantList[i].restaurant.name.search(userInput) === 0) {
      suggestedRestaurants.push(restaurantList[i].restaurant.name);
    }
  }
  return suggestedRestaurants;
}

